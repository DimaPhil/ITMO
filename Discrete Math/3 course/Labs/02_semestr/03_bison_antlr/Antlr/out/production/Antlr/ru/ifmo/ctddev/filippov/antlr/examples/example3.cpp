#include <bits/stdc++.h>

int a, b;

int main() {
  a = 5;
  b = 4;
  if (a>b) {
      std::cout << "5 > 4" << std::endl;
      ;;
  } else {
      std::cout << "5 <= 4";
      ;;
  };
  a = a - 2;
  if (a>b) {
      std::cout << "5 > 4" << std::endl;
      ;;
  } else {
      std::cout << "5 <= 4";
      ;;
  };
  ;
  return 0;
}

